step = input("Введите направление (straight,back,left,right): ")
a = "straight"
b = "back"
c = "left"
d = "right"
if a == step:
    print("Иду прямо")
elif b == step:
    print("Иду назад:")
elif c == step:
    print("Иду влево")
elif d == step:
    print("Иду вправо")
else:
    print("Неправильное направление")
    
  
